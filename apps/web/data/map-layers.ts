const parseGeoJson = (coords: any[]): { lat: number; lng: number }[] => {
  const isNested = Array.isArray(coords[0]) && Array.isArray(coords[0][0]);
  const activeArray = isNested ? coords[0] : coords;

  return activeArray.map((point: any) => ({
    lat: Number(point[1]),
    lng: Number(point[0]),
  }));
};

